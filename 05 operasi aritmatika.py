# operasi aritmatika

a = 10
b = 3

# penjumlahan +
hasil = a + b
print(a, "+", b, "=", hasil)

# pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# pangkat eksponen **
hasil = a ** b
print(a, "^", b, "=", hasil)

# modulus (sisa pembagian) %
hasil = a % b
print(a, "%", b, "=", hasil)

# floor division (hasil dgn pembulatan ke bawah) //
hasil = a // b
print(a, "%//", b, "=", hasil)

# prioritas operasi ; operational precendence
'''
1. tanda kurung ()
2. eksponen **
3. perkalian dan kawan-kawan * / ** // %
4. penjumlahan dan pengurangan + -
'''